-- ============================================
-- קובץ אחד שמריץ הכל בסדר הנכון
-- ============================================

-- 1. תיקון constraint
ALTER TABLE menu_items DROP CONSTRAINT IF EXISTS menu_items_category_check;
ALTER TABLE menu_items ADD CONSTRAINT menu_items_category_check
  CHECK (category IN ('food', 'alcohol', 'wine', 'desserts', 'sushi'));

-- 2. עמודת notes לשינויים
ALTER TABLE menu_items ADD COLUMN IF NOT EXISTS notes TEXT;

-- 3. הרשאות storage
DO $$ BEGIN
  CREATE POLICY "authenticated update images" ON storage.objects
    FOR UPDATE TO authenticated USING (bucket_id = 'menu-images');
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

DO $$ BEGIN
  CREATE POLICY "authenticated delete images" ON storage.objects
    FOR DELETE TO authenticated USING (bucket_id = 'menu-images');
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

-- 4. הרשאות עריכה ומחיקה על תפריט
DO $$ BEGIN
  CREATE POLICY "authenticated update menu" ON menu_items
    FOR UPDATE TO authenticated USING (true);
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

DO $$ BEGIN
  CREATE POLICY "authenticated delete menu" ON menu_items
    FOR DELETE TO authenticated USING (true);
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

-- 5. מחיקת נתונים קיימים (למניעת כפילויות)
DELETE FROM menu_items WHERE category IN ('desserts', 'wine', 'alcohol', 'sushi');

